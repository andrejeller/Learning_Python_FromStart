def metade(preco):
    res = preco / 2
    return res


def dobro(preco):
    res = preco * 2
    return res


def aumentar(preco, porcentagem):
    res = preco + (preco * porcentagem/100)
    return res


def diminuir(preco, porcentagem):
    res = preco - (preco * porcentagem/100)
    return res


